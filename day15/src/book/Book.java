package book;

public class Book {

	private String name;
	private  double price;
	
	public double  getOriginalPrice() {
		return this.price;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getPrice() {
		return this. price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
